print(f"Division: {a / b}")
print(f"Modulus: {a % b}")

#4 Temperature Converter
celsius = float(input("Enter temperature in Celsius: "))
fahrenheit = (celsius * 9/5) + 32
print(f"Temperature in Fahrenheit: {fahrenheit}")





